package com.emorphis.cashmanagement.service;

import java.util.List;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import com.emorphis.cashmanagement.dao.SecurityUserDao;
import com.emorphis.cashmanagement.model.User;


@Service("userService")
@Transactional
public class UserServiceImpl implements SecurityUserService{

	@Autowired
	private SecurityUserDao dao;

//	@Autowired
//    private PasswordEncoder passwordEncoder;
	
	public User findById(String id) {
		return dao.findById(id);
	}

	public User findBySSO(String sso) {
		User user = dao.findBySSO(sso);
		return user;
	}

	public void saveUser(User user) {
//		user.setUserPassword(passwordEncoder.encode(user.getUserPassword()));
		dao.save(user);
	}

	/*
	 * Since the method is running with Transaction, No need to call hibernate update explicitly.
	 * Just fetch the entity from db and update it with proper values within transaction.
	 * It will be updated in db once transaction ends. 
	 */
	public void updateUser(User user) {
		User entity = dao.findById(user.getId());
		if(entity!=null){
			entity.setUserId(user.getUserId());
			if(!user.getUserPassword().equals(entity.getUserPassword())){
//				entity.setUserPassword(passwordEncoder.encode(user.getUserPassword()));
			}
			entity.setFirstName(user.getFirstName());
			entity.setLastName(user.getLastName());
			entity.setEmail(user.getEmail());
			entity.setUserRoles(user.getUserRoles());
		}
	}

	
	public void deleteUserBySSO(String sso) {
		dao.deleteBySSO(sso);
	}

	public List<User> findAllUsers() {
		return dao.findAllUsers();
	}

	public boolean isUserSSOUnique(String id, String sso) {
		User user = findBySSO(sso);
		return ( user == null || ((id != null) && (user.getId() == id)));
	}
	
}
